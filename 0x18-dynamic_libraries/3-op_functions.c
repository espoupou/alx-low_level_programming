/**
 * add - adds two numbers.
 * @a: first number.
 * @b: second number.
 * Return: add.
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subctracts two numbers.
 * @a: first number.
 * @b: second number.
 * Return: difference.
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two numbers.
 * @a: first number.
 * @b: second number.
 * Return: multiplication.
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two numbers.
 * @a: first number.
 * @b: second number.
 * Return: division.
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - calculates the module of two numbers.
 * @a: first number.
 * @b: second number.
 * Return: modulo.
 */
int mod(int a, int b)
{
	return (a % b);
}
